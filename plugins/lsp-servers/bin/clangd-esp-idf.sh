#!/bin/bash
# Wrapper for esp-clang from ESP-IDF
# Automatically detects the installed version in ~/.espressif
#
# Default override: export ESPRESSIF_DIR=/custom/path

ESPRESSIF_DIR="${ESPRESSIF_DIR:-$HOME/.espressif}"
TOOLS_DIR="$ESPRESSIF_DIR/tools"

# Detects the most recently installed esp-clang
ESP_CLANG=$(ls "$TOOLS_DIR/esp-clang"/*/esp-clang/bin/clangd 2>/dev/null | sort -V | tail -1)

if [ -z "$ESP_CLANG" ]; then
    echo "ERROR: esp-clang not found in $TOOLS_DIR/esp-clang" >&2
    echo "Install it with: idf_tools.py install esp-clang" >&2
    exit 1
fi

# Build query-driver with expanded bin directories.
# Passing resolved paths avoids clangd having to expand version globs itself,
# which is unreliable across platforms and clangd versions.
QUERY_PARTS=()
for bin_dir in "$TOOLS_DIR/xtensa-esp-elf"/*/xtensa-esp-elf/bin; do
    [[ -d "$bin_dir" ]] && QUERY_PARTS+=("$bin_dir/xtensa-esp*-elf-*")
done
for bin_dir in "$TOOLS_DIR/riscv32-esp-elf"/*/riscv32-esp-elf/bin; do
    [[ -d "$bin_dir" ]] && QUERY_PARTS+=("$bin_dir/riscv32-esp*-elf-*")
done
QUERY_DRIVER=$(IFS=','; echo "${QUERY_PARTS[*]}")

# Update ~/.config/clangd/config.yaml with C++ stdlib includes from the
# Xtensa GCC toolchain. clangd reads this file automatically on startup.
# This is a fallback for when query-driver alone cannot resolve cross-compiler
# system headers (e.g. std::string, uint32_t) in the Claude Code LSP context.
_update_clangd_user_config() {
    local config_dir="$HOME/.config/clangd"
    local config_file="$config_dir/config.yaml"
    local marker_begin="# --- BEGIN esp-idf-toolchain (auto-generated by clangd-esp-idf.sh) ---"
    local marker_end="# --- END esp-idf-toolchain ---"

    local includes=()
    for xtensa_root in "$TOOLS_DIR/xtensa-esp-elf"/*/xtensa-esp-elf; do
        [[ -d "$xtensa_root" ]] || continue
        local inner="$xtensa_root/xtensa-esp-elf"
        local cxx_ver
        cxx_ver=$(ls "$inner/include/c++/" 2>/dev/null | sort -V | tail -1)
        [[ -z "$cxx_ver" ]] && continue
        includes+=("    - \"-isystem$inner/include/c++/$cxx_ver\"")
        includes+=("    - \"-isystem$inner/include/c++/$cxx_ver/xtensa-esp-elf\"")
        includes+=("    - \"-isystem$inner/include/c++/$cxx_ver/backward\"")
        includes+=("    - \"-isystem$inner/include\"")
        includes+=("    - \"-isystem$inner/sys-include\"")
    done

    [[ ${#includes[@]} -eq 0 ]] && return

    local new_block
    new_block=$(printf '%s\n' \
        "$marker_begin" \
        "CompileFlags:" \
        "  Add:" \
        "${includes[@]}" \
        "$marker_end")

    mkdir -p "$config_dir"

    if [[ ! -f "$config_file" ]]; then
        echo "$new_block" > "$config_file"
        return
    fi

    # Replace existing auto-generated section, or append if absent
    if grep -qF "$marker_begin" "$config_file"; then
        python3 - "$config_file" "$marker_begin" "$marker_end" "$new_block" <<'PYEOF'
import sys
path, begin, end, new_block = sys.argv[1], sys.argv[2], sys.argv[3], sys.argv[4]
content = open(path).read()
s = content.find(begin)
e = content.find(end)
if s != -1 and e != -1:
    result = content[:s] + new_block + content[e + len(end):]
    open(path, 'w').write(result)
PYEOF
    else
        printf '\n%s\n' "$new_block" >> "$config_file"
    fi
}

_update_clangd_user_config

exec "$ESP_CLANG" "--query-driver=$QUERY_DRIVER" "$@"
